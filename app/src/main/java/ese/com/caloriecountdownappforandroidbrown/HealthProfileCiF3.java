        public String ReckonClientAge()
        {


            if(DOBday > 0 && DOBmonth > 0 && DOByear > 0)
            {
                Calendar Greg = Calendar.getInstance();
                Greg.set(Calendar.MONTH, DOBmonth);
                Greg.set(Calendar.DAY_OF_MONTH, DOBday);
                Greg.set(Calendar.YEAR, DOByear);

                Long today = (new Date()).getTime();
                Long birth = Greg.getTimeInMillis();
                Long age = today - birth;

                int ageInYears = (int)(((((age/1000)/60)/60)/24)/365.25);

                return new RoundingCIF13().IntToString(ageInYears);
            }
            else
            {
                return "18";
            }

        }

